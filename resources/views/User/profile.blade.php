	<h2>DESKRIPSI</h2>
	<p>
		Pemberdayaan Umat Berkelanjutan (PUB) adalah program beasiswa yang diberikan oleh Universitas Nasional PASIM kepada orang yang memiliki kemampuan diatas rata-rata tetapi kurang dalam segi ekonomi.
		Di program PUB mahasiswa PUB langsung dijuruskan pada program studi D3 Manajemen Informatika selama 2 tahun masa pendidikan dan 1 tahun terakhir sudah bisa bekerja sambil menyusun tugas akhir, selain menerima program perkuliahan, mahasiswa PUB juga menerima berbagai pelatihan yang wajib diikuti seperti program pelatihan pemrograman, program kerohanian seperti pengajian dan hafalan surat, program magang serta ikut serta dalam kepengurusan organisasi PUB.
	</p>
	<p>
		Setelah selesai mengikuti program perkuliahan, mahasiswa PUB diarahkan untuk langsung bekerja di berbagai perusahaan, untuk bekerja di bidang IT seperti system analiyst, programmer, desainer, implementor dan networking. Yang bermitra dengan atau tanpa bermitra dengan Universitas Nasional PASIM dibidang informatika, karena setiap tahunnya selalu banyak permintaan dari berbagai perusahaan.
	</p>
	<p>
		Program PUB dilaksanakan pada tahun 2002 sampai sekarang, dan telah banyak menyerap sebanyak kurang lebih sebanyak 450 orang yang terdiri dari 14 angkatan. Program PUB telah menghasilkan lulusan sebanyak 300 orang yang telah bekerja di berbagai perusahaan yang ada di Indonesia. Berkat bantuan alumni mahasiswa PUB untuk setiap tahunnya program ini semakin banyak menyerap orang-orang yang memiliki kemampuan di atas rata-rata dari kalangan tidak mampu secara ekonomi. Program ini pendanaan berasal dari Infaq Alumni dan Yayasan. Jumlah Siswa PUB yang direkrut kurang lebih 60 siswa per tahunnya. 
